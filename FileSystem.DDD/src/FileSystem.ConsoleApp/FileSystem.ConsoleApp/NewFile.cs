namespace FileSystem.ConsoleApp;

/// <summary>
/// Creation of a new file
/// </summary>
public class NewFile
{
    /// <summary>
    /// Name of the file
    /// </summary>
    public string Name { get; set; }
    
    /// <summary>
    /// Collection of subdirectories
    /// </summary>
    public string[] SubDirectories { get; set; }
    
    /// <summary>
    /// Root directory
    /// </summary>
    public string Root { get; set; }

}